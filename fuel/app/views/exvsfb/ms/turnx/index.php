<h4>■備考</h4>
<p>・月光蝶について<br>
    月光蝶コンボをよく使う人はB覚醒の方がいいと思う<br>
    TXはB覚防御補正が-30%で恩恵がデカい<br>
    通常のズンダ158が110.6の被ダメになる<br>
    自分の体力が120以上あれば後述するコンボを積極的に狙うのもありかもしれない
</p>

<p>・バグ？<br>
    A覚醒時「メイン>>メイン>>メイン」は計算値189だが、実測値は190<br>
    A覚醒時「メイン>>メイン→CS」は計算値217だが、実測値は218<br>
    計算式は多分合っているはずなので、ゲーム(言語？)のバグの可能性が高い
</p>
<p>・以下駄文<br>
    浮動小数点数の計算は開発言語によってバグが出る<br>
    PHPはバグだらけ<br>
    COBOLは詳しくないのですが、浮動小数点数の計算はとても強いらしいです
</p>

