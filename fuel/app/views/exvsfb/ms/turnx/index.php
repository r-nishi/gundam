<h4>■おすすめコンボ</h4>

<p><b>・メイン>>後>>NN→CS 191</b><br>
    メイン後、格闘入れるには遠すぎるが、ズンダするには安すぎる時に使う中距離用コンボ<br>
    サーチ変え可能な所も良い<br>
    TXにひたすら乗ってアンカーの距離を覚えれば自然とできるようになる
</p>

<p><b>・横>>NNN→CS 239</b><br>
    基本<br>
    CSをためていなかった場合、締めはメインで221、サブ締めで226<br>
    特格は掴みしか入らないが、覚醒中であれば爆発まで入る
</p>

<p><b>・後>>NNN→CS 234</b><br>
    こちらは締めのCSを特格に変更した場合、爆発まで入る(244ダメ)<br>
    回避行動の為にNNで攻撃をキャンセルした場合締めをメイン→サブにしないとダウンしない
</p>

<p><b>・後>>横N2>>月光蝶 359(A覚醒)</b><br>
    一番よく使う月光蝶コン、コスオバした後衛2000を溶かす<br>
    コスオバしたサンドロック(体力290)にB覚防御補正がかかるとギリギリ落とせない<br>
    計算式→月光蝶威力(359) * サンドロック防御補正(0.8) = 与ダメ287.2<br>
    <br>
    コスオバしたブリッツ(体力270)ならB覚吐こうが元の体力が低いため月光蝶コンで落とせる<br>
    頭の片隅に入れておき、逆転を狙おう
</p>