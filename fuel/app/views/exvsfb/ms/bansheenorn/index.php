<p>・後格闘カウンター<br>
    後(スタン)はダメージ、補正、ダウン値が0なので通常のコンボレシピを選択可能
</p>
<p>・立ち回り<br>
    格CS、メインCSは優秀だがメインバラ撒いてるほうが強い<br>
    また、溜めている間はサブが使えなくなるのでデメリットの方が多いまである
</p>

